-- 1. CATEGORIES (normalized; replaces free-text category column)
-- ============================================================
create table if not exists public.categories (
  id          bigint generated by default as identity primary key,
  name        text   not null,
  slug        text   not null unique,
  description text,
  display_order integer not null default 0,
  active      boolean not null default true,
  created_at  timestamptz not null default now(),
  updated_at  timestamptz not null default now()
);

drop trigger if exists categories_set_updated_at on public.categories;
create trigger categories_set_updated_at
  before update on public.categories
  for each row execute function public.set_updated_at();

create index if not exists categories_slug_idx   on public.categories (slug);
create index if not exists categories_active_idx on public.categories (active);

alter table public.categories enable row level security;

do $$ begin
  if not exists (select 1 from pg_policies where schemaname='public' and tablename='categories' and policyname='categories_public_read') then
    create policy categories_public_read on public.categories for select using (true);
  end if;
  if not exists (select 1 from pg_policies where schemaname='public' and tablename='categories' and policyname='categories_service_write') then
    create policy categories_service_write on public.categories for all to service_role using (true) with check (true);
  end if;
end $$;

