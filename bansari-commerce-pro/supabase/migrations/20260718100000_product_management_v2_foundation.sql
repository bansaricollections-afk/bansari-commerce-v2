-- ------------------------------------------------------------
-- HELPER: ensure set_updated_at function exists (idempotent)
-- ------------------------------------------------------------
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

